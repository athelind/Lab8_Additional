class Operation:
    def add(num1, num2):
        result = num1 + num2
        return result

    def difference(num1, num2):
        result = num1 - num2
        return result

    def product(num1, num2):
        result = num1 * num2
        return result

    def division(num1, num2):
        result = num1 / num2
        return result